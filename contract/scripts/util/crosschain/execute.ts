const main = async () => {
  const sdkClient = createClient({
    baseApiUrl: MAINNET_RELAY_API,
    chains: [convertViemChainToRelayChain(base)],
  })

  const account = privateKeyToAccount(
    process.env.TEST_PRIVATE_KEY! as `0x${string}`
  )
  const walletClient = createWalletClient({
    chain: base,
    transport: http(),
    account,
  })

  const publicClient = createPublicClient({
    chain: degen,
    transport: http(),
  })

  const { request } = await publicClient.simulateContract({
    address: "0x073E40373193B2708fB560f897138c88dC7f1230",
    abi: GashaItemABI,
    functionName: "mint",
    args: ["0x807C69F16456F92ab2bFc9De8f14AF31051f9678", BigInt(2)],
    account,
  })

  try {
    const result = await sdkClient.actions.call({
      chainId: base.id,
      toChainId: degen.id,
      txs: [request],
      wallet: walletClient,
    })

    console.log(result)
  } catch (error) {
    console.error(error)
  }
}

main()
